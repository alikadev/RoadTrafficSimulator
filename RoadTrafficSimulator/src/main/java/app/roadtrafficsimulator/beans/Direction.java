package app.roadtrafficsimulator.beans;

/**
 * Represents the possible directions for road traffic simulation.
 * The directions are LEFT, TOP, RIGHT, and DOWN.
 *
 * @autor Elvin Kuci
 */
public enum Direction {
    /**
     * Represents the left direction.
     */
    LEFT,

    /**
     * Represents the top direction.
     */
    TOP,

    /**
     * Represents the right direction.
     */
    RIGHT,

    /**
     * Represents the down direction.
     */
    DOWN
}
